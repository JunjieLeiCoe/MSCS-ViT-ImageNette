# --- Training Function ---
def train_model(model, train_loader, test_loader, num_epochs, optimizer, criterion, scheduler=None,
                is_noise_curriculum=False, dataset_obj=None):
    history = {'train_loss': [], 'train_acc': [], 'val_loss': [], 'val_acc': []}

    current_run_name = "Noise-Only Curriculum (Decreasing Noise)" if is_noise_curriculum else "Baseline"
    print(f"\n--- Starting Training: {current_run_name} ---")

    for epoch in range(num_epochs):
        model.train()
        running_loss = 0.0
        correct_train = 0
        total_train = 0

        if is_noise_curriculum and dataset_obj is not None:
            epochs_for_progression = max(1, num_epochs - 10) # Epochs over which curriculum changes
            # current_noise_pct will DECREASE from 1.0 to 0.0

            if epoch >= epochs_for_progression:
                current_noise_pct = 0.0 # Min noise (clear image) in the final stage
            else:
                progress = epoch / epochs_for_progression # progress goes from 0 to 1
                current_noise_pct = 1.0 - progress # Noise decreases from 1.0 down to 0.0

            dataset_obj.set_noise_std_percentage(current_noise_pct)
            dataset_obj.set_cutoff_percentage(1.0) # Ensure no blur

            print(f"Epoch {epoch+1}/{num_epochs} ({current_run_name}) - Curriculum Update: "
                  f"NoisePct={current_noise_pct:.3f} " # NP closer to 0 means clearer
                  f"(AbsStd~{current_noise_pct*dataset_obj.max_noise_std_absolute:.3f})")

        for inputs, labels in tqdm(train_loader, desc=f"Epoch {epoch+1}/{num_epochs} (Train)", leave=False):
            inputs, labels = inputs.to(DEVICE), labels.to(DEVICE)
            optimizer.zero_grad()
            outputs = model(inputs)
            loss = criterion(outputs, labels)
            loss.backward()
            optimizer.step()
            running_loss += loss.item() * inputs.size(0)
            _, predicted = torch.max(outputs.data, 1)
            total_train += labels.size(0)
            correct_train += (predicted == labels).sum().item()

        epoch_train_loss = running_loss / total_train
        epoch_train_acc = correct_train / total_train
        history['train_loss'].append(epoch_train_loss)
        history['train_acc'].append(epoch_train_acc)

        model.eval()
        val_loss = 0.0
        correct_val = 0
        total_val = 0
        with torch.no_grad():
            for inputs, labels in tqdm(test_loader, desc=f"Epoch {epoch+1}/{num_epochs} (Val)", leave=False):
                inputs, labels = inputs.to(DEVICE), labels.to(DEVICE)
                outputs = model(inputs)
                loss_val_batch = criterion(outputs, labels)
                val_loss += loss_val_batch.item() * inputs.size(0)
                _, predicted_val = torch.max(outputs.data, 1)
                total_val += labels.size(0)
                correct_val += (predicted_val == labels).sum().item()

        epoch_val_loss = val_loss / total_val
        epoch_val_acc = correct_val / total_val
        history['val_loss'].append(epoch_val_loss)
        history['val_acc'].append(epoch_val_acc)

        print(f"Epoch {epoch+1}/{num_epochs} ({current_run_name}) - "
              f"Train Loss: {epoch_train_loss:.4f} Acc: {epoch_train_acc:.4f} | "
              f"Val Loss: {epoch_val_loss:.4f} Acc: {epoch_val_acc:.4f}")

        if scheduler:
            scheduler.step()
    print(f"--- Training Complete: {current_run_name} ---")
    return history

# --- Visualization of Degraded Images (Noise-Only, DECREASING NOISE) ---
print("\n--- Visualizing Noise-Degraded Images (Decreasing Noise) ---")
def visualize_noise_degradations(dataset_obj_vis, vis_epochs_indices, num_images=4, total_epochs_for_sched=NUM_EPOCHS):
    original_transform = dataset_obj_vis.transform
    dataset_obj_vis.transform = None

    fig, axes = plt.subplots(len(vis_epochs_indices), num_images,
                             figsize=(num_images * 2.5, len(vis_epochs_indices) * 2.5))
    if len(vis_epochs_indices) == 1: axes = axes.reshape(1, -1)

    epochs_for_progression_vis = max(1, total_epochs_for_sched - 10)

    for i, epoch_idx_sim in enumerate(vis_epochs_indices):
        temp_noise_pct = 0.0 # Default to min noise
        if epoch_idx_sim >= epochs_for_progression_vis:
            temp_noise_pct = 0.0 # Min noise (clear)
        else:
            progress_vis = epoch_idx_sim / epochs_for_progression_vis if epochs_for_progression_vis > 0 else 1.0
            temp_noise_pct = 1.0 - progress_vis # Noise decreases from 1.0

        dataset_obj_vis.set_noise_std_percentage(temp_noise_pct)
        dataset_obj_vis.set_cutoff_percentage(1.0)

        raw_images_pil = []
        num_samples_to_fetch = min(num_images, len(dataset_obj_vis))
        indices_to_fetch = np.random.choice(len(dataset_obj_vis), num_samples_to_fetch, replace=False) if num_samples_to_fetch > 0 else []
        for k_idx in indices_to_fetch:
            img_pil, _ = dataset_obj_vis[k_idx]
            raw_images_pil.append(img_pil)

        for j in range(min(num_images, len(raw_images_pil))):
            img_pil = raw_images_pil[j]
            ax = axes[i, j]
            ax.imshow(img_pil)
            # NP closer to 0 means clearer image
            ax.set_title(f'E{epoch_idx_sim+1}\nNP={temp_noise_pct:.2f}', fontsize=8)
            ax.axis('off')
        for j_extra in range(len(raw_images_pil), num_images):
            if i < axes.shape[0] and j_extra < axes.shape[1]: axes[i, j_extra].axis('off')

    fig.suptitle("Image Samples: Decreasing Noise Curriculum Stages", fontsize=14)
    plt.tight_layout(rect=[0, 0.03, 1, 0.95])
    plt.show()
    dataset_obj_vis.transform = original_transform

