class CIFAR10DegradationDataset(CIFAR10):
    def __init__(self, root, train=True, transform=None, download=False, cutoff_percentage=1.0, noise_std_percentage=0.0):
        super().__init__(root, train=train, transform=transform, download=download)
        self.cutoff_percentage = 1.0 # Fixed: No blur in this version
        self.max_noise_std_absolute = MAX_NOISE_STD_ABSOLUTE
        # noise_std_percentage: 1.0 = max noise, 0.0 = no noise
        self.set_noise_std_percentage(noise_std_percentage)


    def set_cutoff_percentage(self, pct: float):
        self.cutoff_percentage = 1.0 # Always ensure no blur

    def set_noise_std_percentage(self, pct: float):
        if not (0.0 <= pct <= 1.0):
            self.noise_std_percentage = max(0.0, min(1.0, pct))
        else:
            self.noise_std_percentage = pct

    def __getitem__(self, index):
        img_np = self.data[index]
        target = self.targets[index]
        img_float = np.float32(img_np) / 255.0
        img_processed_color = img_float

        # --- Apply Additive Gaussian Noise ---
        if self.noise_std_percentage > 0.0: # Only add if noise percentage is > 0
            current_noise_std = self.noise_std_percentage * self.max_noise_std_absolute
            noise = np.random.normal(0, current_noise_std, img_processed_color.shape).astype(np.float32)
            img_processed_color = img_processed_color + noise
            img_processed_color = np.clip(img_processed_color, 0.0, 1.0)

        img_pil = Image.fromarray(np.uint8(img_processed_color * 255.0))
        if self.transform is not None:
            img_pil = self.transform(img_pil)
        return img_pil, target

